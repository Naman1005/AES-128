void AES128::mixColumns(uint8_t *state)
{
    for (int i = 0; i < 4; ++i)
    {
        int idx = i * 4;
        uint8_t a = state[idx], b = state[idx + 1], c = state[idx + 2], d = state[idx + 3];
        state[idx] = xtime(a) ^ xtime(b) ^ b ^ c ^ d;
        state[idx + 1] = a ^ xtime(b) ^ xtime(c) ^ c ^ d;
        state[idx + 2] = a ^ b ^ xtime(c) ^ xtime(d) ^ d;
        state[idx + 3] = xtime(a) ^ a ^ b ^ c ^ xtime(d);
    }
}

void AES128::invMixColumns(uint8_t *state)
{
    for (int i = 0; i < 4; ++i)
    {
        int idx = i * 4;
        uint8_t a = state[idx], b = state[idx + 1], c = state[idx + 2], d = state[idx + 3];
        state[idx] = mul(a, 0x0e) ^ mul(b, 0x0b) ^ mul(c, 0x0d) ^ mul(d, 0x09);
        state[idx + 1] = mul(a, 0x09) ^ mul(b, 0x0e) ^ mul(c, 0x0b) ^ mul(d, 0x0d);
        state[idx + 2] = mul(a, 0x0d) ^ mul(b, 0x09) ^ mul(c, 0x0e) ^ mul(d, 0x0b);
        state[idx + 3] = mul(a, 0x0b) ^ mul(b, 0x0d) ^ mul(c, 0x09) ^ mul(d, 0x0e);
    }
}

// Helper for multiplication in GF(2^8)
uint8_t AES128::mul(uint8_t x, uint8_t y)
{
    uint8_t r = 0;
    for (int i = 0; i < 8; ++i)
    {
        if (y & 1)
            r ^= x;
        bool h = x & 0x80;
        x <<= 1;
        if (h)
            x ^= 0x1b;
        y >>= 1;
    }
    return r;
}

void AES128::encryptBlock(uint8_t *state)
{
    addRoundKey(state, 0);
    for (int round = 1; round < 10; ++round)
    {
        subBytes(state);
        shiftRows(state);
        mixColumns(state);
        addRoundKey(state, round);
    }
    subBytes(state);
    shiftRows(state);
    addRoundKey(state, 10);
}

void AES128::decryptBlock(uint8_t *state)
{
    addRoundKey(state, 10);
    for (int round = 9; round > 0; --round)
    {
        invShiftRows(state);
        invSubBytes(state);
        addRoundKey(state, round);
        invMixColumns(state);
    }
    invShiftRows(state);
    invSubBytes(state);
    addRoundKey(state, 0);
}
