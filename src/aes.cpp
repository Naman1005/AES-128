static const uint8_t Rcon[11] = {
    0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36};

static uint8_t xtime(uint8_t x)
{
    return (x << 1) ^ ((x >> 7) * 0x1b);
}

// Removed unused xorBlock function to silence compiler warning

static void copyBlock(uint8_t *dst, const uint8_t *src)
{
    for (int i = 0; i < 16; ++i)
        dst[i] = src[i];
}

AES128::AES128(const std::vector<uint8_t> &key)
{
    if (key.size() != 16)
        throw std::invalid_argument("Key must be 16 bytes for AES-128");
    keyExpansion(key);
}

std::vector<uint8_t> AES128::encrypt(const std::vector<uint8_t> &data)
{
    std::vector<uint8_t> padded = data;
    size_t pad = 16 - (padded.size() % 16);
    padded.insert(padded.end(), pad, static_cast<uint8_t>(pad));
    std::vector<uint8_t> out(padded.size());
    for (size_t i = 0; i < padded.size(); i += 16)
    {
        uint8_t block[16];
        copyBlock(block, &padded[i]);
        encryptBlock(block);
        std::copy(block, block + 16, out.begin() + i);
    }
    return out;
}

std::vector<uint8_t> AES128::decrypt(const std::vector<uint8_t> &data)
{
    if (data.size() % 16 != 0)
        throw std::invalid_argument("Invalid ciphertext length");
    std::vector<uint8_t> out(data.size());
    for (size_t i = 0; i < data.size(); i += 16)
    {
        uint8_t block[16];
        copyBlock(block, &data[i]);
        decryptBlock(block);
        std::copy(block, block + 16, out.begin() + i);
    }
    // Remove PKCS#7 padding
    uint8_t pad = out.back();
    if (pad == 0 || pad > 16)
        throw std::runtime_error("Invalid padding");
    for (size_t i = out.size() - pad; i < out.size(); ++i)
    {
        if (out[i] != pad)
            throw std::runtime_error("Invalid padding");
    }
    out.resize(out.size() - pad);
    return out;
}

void AES128::keyExpansion(const std::vector<uint8_t> &key)
{
    roundKeys.resize(176);
    for (int i = 0; i < 16; ++i)
        roundKeys[i] = key[i];
    for (int i = 16, r = 1; i < 176; i += 4)
    {
        uint8_t t[4];
        for (int j = 0; j < 4; ++j)
            t[j] = roundKeys[i - 4 + j];
        if (i % 16 == 0)
        {
            uint8_t tmp = t[0];
            t[0] = sbox[t[1]] ^ Rcon[r];
            t[1] = sbox[t[2]];
            t[2] = sbox[t[3]];
            t[3] = sbox[tmp];
            ++r;
        }
        for (int j = 0; j < 4; ++j)
            roundKeys[i + j] = roundKeys[i - 16 + j] ^ t[j];
    }
}

void AES128::addRoundKey(uint8_t *state, int round)
{
    for (int i = 0; i < 16; ++i)
        state[i] ^= roundKeys[round * 16 + i];
}

void AES128::subBytes(uint8_t *state)
{
    for (int i = 0; i < 16; ++i)
        state[i] = sbox[state[i]];
}

void AES128::invSubBytes(uint8_t *state)
{
    for (int i = 0; i < 16; ++i)
        state[i] = rsbox[state[i]];
}

